
import '../../../domain/entities/CategoryOrBrandResponseEntity.dart';

class CategoryOrBrandResponseDto extends CategoryOrBrandResponseEntity {
  CategoryOrBrandResponseDto({
    super.results,
    this.metadata,
    this.message,
    super.data,
  });

  CategoryOrBrandResponseDto.fromJson(dynamic json) {
    results = json['results'];
    message = json['message'];
    metadata =
        json['metadata'] != null ? Metadata.fromJson(json['metadata']) : null;
    if (json['data'] != null) {
      data = [];
      json['data'].forEach((v) {
        data?.add(CategoryOrBrandDto.fromJson(v));
      });
    }
  }

  Metadata? metadata;
  String? message;
}

